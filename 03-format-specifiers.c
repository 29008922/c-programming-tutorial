/*
FORMAT SPECIFIER
What are format specifiers and why do we use them?

Format specifiers in C are used to take inputs and
print the output of a type. The symbol we use in every
format specifier is %. Format specifiers tell the compiler
about the type of data that must be given as input and
the type of data that must be printed on the screen.

Types
1. Int - %d or %i
2. Float - %f
3. Double - %lf
4. Char - %c
*/